package ui

import "strings"

// splitArt trims leading/trailing newlines and splits into lines.
func splitArt(s string) []string {
	return strings.Split(strings.Trim(s, "\n"), "\n")
}

// ArtNaturalWidth returns the maximum visible rune-width of the art lines,
// ignoring trailing spaces. This is used to size the panel to fit the art
// exactly so no cropping occurs.
func ArtNaturalWidth(artLines []string) int {
	max := 0
	for _, l := range artLines {
		trimmed := strings.TrimRight(l, " ")
		if rw := len([]rune(trimmed)); rw > max {
			max = rw
		}
	}
	return max
}

// SectionAsciiArt returns the decorative ASCII art panel lines for the given
// main section index. The caller is responsible for sizing the panel to
// ArtNaturalWidth(result) so the art is shown in full without cropping.
func SectionAsciiArt(sectionIdx int) []string {
	switch sectionIdx {
	case 0:
		return artDescription
	case 1:
		return artBackend
	case 2:
		return artData
	case 3:
		return artContracts
	case 4:
		return artFrontend
	case 5:
		return artInfra
	case 6:
		return artCrosscut
	case 7:
		return artRealize
	default:
		return artDefault
	}
}

